import DiffableTextKit
import Foundation

//*============================================================================*
// MARK: Number
//*============================================================================*

extension OptionalNumberTextStyle where Format: NumberTextFormatXNumber {
    
    //=------------------------------------------------------------------------=
    // MARK: Initializers
    //=------------------------------------------------------------------------=
    
    @inlinable public init(locale: Locale = .autoupdatingCurrent) {
        self.init(Format(locale: locale))
    }
}

//=----------------------------------------------------------------------------=
// MARK: Decimal
//=----------------------------------------------------------------------------=

extension DiffableTextStyle where Self == OptionalNumberTextStyle<Decimal> {
    
    //=------------------------------------------------------------------------=
    // MARK: Initializers
    //=------------------------------------------------------------------------=
    
    @inlinable public static var number: Self {
