@usableFromInline enum Digit: UInt8, CaseIterable, Component {
    
    //=------------------------------------------------------------------------=
    // MARK: Instances
    //=------------------------------------------------------------------------=
    
    case zero  = 48 // "0"
    case one   = 49 // "1"
    case two   = 50 // "2"
    case three = 51 // "3"
    case four  = 52 // "4"
    case five  = 53 // "5"
    case six   = 54 // "6"
    case seven = 55 // "7"
    case eight = 56 // "8"
    case nine  = 57 // "9"
    
    //=------------------------------------------------------------------------=
    // MARK: Checks
    //=------------------------------------------------------------------------=
    
    @inlinable var isZero: Bool {
        self == .zero
    }
    
    //=------------------------------------------------------------------------=
    // MARK: Value
    //=------------------------------------------------------------------------=
    
    @inlinable var uInt8: UInt8 {
        rawValue - 48
    }
}
