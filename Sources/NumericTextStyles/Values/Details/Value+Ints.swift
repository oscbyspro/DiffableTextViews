//*============================================================================*
// MARK: * Int
//*============================================================================*

extension Int: IntegerValue {
    
    //=------------------------------------------------------------------------=
    // MARK: Precision, Bounds
    //=------------------------------------------------------------------------=
    
    public static let precision: Capacity = precision(String(max).count)
    public static let bounds: ClosedRange<Self> = bounds()
}

