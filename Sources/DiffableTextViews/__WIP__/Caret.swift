//*============================================================================*
// MARK: * Caret
//*============================================================================*
 
/// ```
/// |$|1|2|3|,|4|5|6|.|7|8|9|_|U|S|D|~
/// |>|o|o|o|x|o|o|o|o|o|o|o|<|<|<|<|~
/// ```
@usableFromInline protocol Caret {
    
    //=------------------------------------------------------------------------=
    // MARK: Position
    //=------------------------------------------------------------------------=
    
    @inlinable var position: Snapshot.Index { get }
    
    //=------------------------------------------------------------------------=
    // MARK: Preference
    //=------------------------------------------------------------------------=
    
    @inlinable var preference: Direction { get }
}

//*============================================================================*
// MARK: * Upper
//*============================================================================*

struct Upper: Caret {
    
    // MARK: Properties
    
    @inlinable var index
    
    //=------------------------------------------------------------------------=
    // MARK: Preference
    //=------------------------------------------------------------------------=
    
    @inlinable var preference: Direction {
        .backwards
    }
}

//*============================================================================*
// MARK: * Upper
//*============================================================================*

enum Lower: Caret {
    
    //=------------------------------------------------------------------------=
    // MARK: Preference
    //=------------------------------------------------------------------------=
    
    @inlinable var preference: Direction {
        .forwards
    }
}
