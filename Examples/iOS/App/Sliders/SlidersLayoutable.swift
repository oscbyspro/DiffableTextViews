import SwiftUI

//*============================================================================*
// MARK: * SlidersLayoutable
//*============================================================================*

protocol SlidersLayoutable {
    
    //=------------------------------------------------------------------------=
    // MARK: Storage
    //=------------------------------------------------------------------------=
    
    var layout: SlidersLayout { get }
}

//=----------------------------------------------------------------------------=
// MARK: SlidersStorageable - Details
//=----------------------------------------------------------------------------=

extension SlidersLayoutable {
    
    //=------------------------------------------------------------------------=
    // MARK: Accessors
    //=------------------------------------------------------------------------=
    
    var frame: CGRect {
        layout.frame
    }
    
    var positions: (CGFloat, CGFloat) {
        layout.positions
    }
}
