import SwiftUI

//*============================================================================*
// MARK: * HasLayout
//*============================================================================*

@usableFromInline protocol HasLayout {
    
    //=------------------------------------------------------------------------=
    // MARK: Storage
    //=------------------------------------------------------------------------=
    
    @inlinable var layout: Layout { get }
}

//=----------------------------------------------------------------------------=
// MARK: HasStorage - Details
//=----------------------------------------------------------------------------=

extension HasLayout {
    
    //=------------------------------------------------------------------------=
    // MARK: Accessors
    //=------------------------------------------------------------------------=
    
    @inlinable var frame: CGRect {
        layout.frame
    }
    
    @inlinable var positions: (CGFloat, CGFloat) {
        layout.positions
    }
}
